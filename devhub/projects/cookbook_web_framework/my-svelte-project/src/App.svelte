<script>
	export let name;

	// 新增的状态变量和函数
	let count = 0;

	function increment() {
		count += 1;
	}
</script>

<main>
	<h1>Hello {name}!</h1>
	<p>Visit the <a href="https://svelte.dev/tutorial">Svelte tutorial</a> to learn how to build Svelte apps.</p>

	<!-- 计数器部分 -->
	<p>The count is {count}</p>
	<button on:click={increment}>Increment</button>
</main>

